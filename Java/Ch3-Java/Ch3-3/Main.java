public class Main {

    public static void main(String[] args) {
        int x = 10 + 5;             // 加：x變成15
        int y = 20 - x;             // 減：y變成5
        int z = x * y;              // 乘：z變成75
        double q = (double) x / y;  // 除：q變成3.0
        System.out.println(x + " " + y + " " + z + " " + q);
    }
}
